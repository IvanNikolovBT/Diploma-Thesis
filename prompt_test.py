def create_prompt(target_feature: str, song: str) -> str:
    if target_feature not in style_definitions:
        raise ValueError(f"Feature '{target_feature}' not found in style definitions.")
    
    target_feature_definition = style_definitions[target_feature]
    
    return (
        f"{target_feature_definition}\n"
        "Ова ја претставува дефиницијата, не ја давај нејзе, во твојот одговор.\n\n"
        f"Напиши краток опис дали авторот на следниот пасус ја содржи оваа особина: {target_feature}.\n"
        "Одговори со Да или Не.\n\n"
        f"Пасус:\n{song}\n\nОпис:"
    )
def create_full_prompt(song: str) -> str:
    """
    Creates a single prompt containing all style definitions and asks the model
    to evaluate whether each feature is present in the given passage/song.
    """
    features_text = "\n".join(
        [f"{feature}" for feature,definition in style_definitions.items()]
    )
    
    prompt = (
        "Листа на различни стилски и лингвистички особини:\n\n"
        f"{features_text}\n\n"
        "Проучи го следниот пасус и за секоја од овие особини одговори со 'Да' или 'Не' дали авторот ја користи.\n\n"
        f"Пасус:\n{song}\n\n"
        "Одговор (форматирај по ред: 'Фигуративен јазик: Да', 'Сарказам: Не', ...):"
    )
    
    return prompt

